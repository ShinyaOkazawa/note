<template><div><h1 id="vitepress-について調べてみる" tabindex="-1"><a class="header-anchor" href="#vitepress-について調べてみる"><span>VitePress について調べてみる</span></a></h1>
<p>本ブログを VuePress から VitePress に移行しようと思っているので VitePress について調べることにする。</p>
<h2 id="vitepress-とは" tabindex="-1"><a class="header-anchor" href="#vitepress-とは"><span>VitePress とは</span></a></h2>
<p>高速でコンテンツ中心のウェブサイトを構築するよう設計された静的サイトジェネレータ。
コンテンツは Markdown で書かれる。</p>
<p>Vue.js の公式ドキュメントも VitePress で作られているらしい。</p>
<p>Vite + Vue で作られているからカスタマイズもしやすいと。</p>
<h2 id="vuepress-と-vitepress-の違い" tabindex="-1"><a class="header-anchor" href="#vuepress-と-vitepress-の違い"><span>VuePress と VitePress の違い</span></a></h2>
<p>VuePress は Vue + webpack。
VitePress は Vue + vite。</p>
<p>VuePress のv2からは vite か webpack か選べるっぽい。
VuePress v2 の <a href="https://v2.vuepress.vuejs.org/guide/introduction.html#vitepress" target="_blank" rel="noopener noreferrer">doc<ExternalLinkIcon/></a> に VitePress について触れてる箇所がある。
VitePress は VuePress と比べて軽量で高速だが、トレードオフとして設定が少ない、例えばプラグインをサポートしていないなど。らしい。
VuePress を選ぶか VitePress を選ぶかは、Laravel か Lumen かを選ぶようなフレームワークの違いみたいなものらしい。</p>
<p>逆に VitePress の<a href="https://vitepress.dev/guide/what-is-vitepress#:~:text=There%20has%20also%20been%20effort%20invested%20into%20VuePress%202%2C%20which%20also%20supports%20Vue%203%20and%20Vite%20with%20more%20compatibility%20with%20VuePress%201.%20However%2C%20maintaining%20two%20SSGs%20in%20parallel%20isn%27t%20sustainable%2C%20so%20the%20Vue%20team%20has%20decided%20to%20focus%20on%20VitePress%20as%20the%20main%20recommended%20SSG%20in%20the%20long%20run." target="_blank" rel="noopener noreferrer">ドキュメント<ExternalLinkIcon/></a>も VuePress について触れている。</p>
<p>VuePress と VitePress を並行してメンテナンスするのは持続可能ではないため Vue チームは長期的には VitePress を推奨 SSG とすることに決めたと。
じゃあ VitePress に乗り換えるか。</p>
